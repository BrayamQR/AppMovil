package com.example.appmovil.entidades

class UsuarioDto (){
    var id: Int
        get() = id
        set(id){
            this.id= id
        }
    var user: String
        get() = user
        set(user){
            this.user = user
        }
    var password: String
        get() = password
        set(password){
            this.password = password
        }
    var nombre: String
        get() = nombre
        set(nombre){
            this.nombre = nombre
        }
    var dni: String
        get() = dni
        set(dni){
            this.dni = dni
        }
}